    @Override
    public void setForm(NhaCungCap entity) {
        txtUsername.setText(entity.getUsername());
        txtNumberPhone.setText(entity.getNumberPhone());
        txtAddress.setText(entity.getAddress());

    }

    @Override
    public NhaCungCap getForm() {
        NhaCungCap entity = new NhaCungCap();
        entity.setUsername(txtUsername.getText());
        entity.setNumberPhone(txtNumberPhone.getText());
        entity.setAddress(txtAddress.getText());
        return entity;

    }

    @Override
    public void create() {
        NhaCungCap entity = this.getForm();
        // Kiểm tra username đã tồn tại chưa
        if (dao.findById(entity.getUsername()) != null) {
            XDialog.alert("Tên đăng nhập đã tồn tại! Vui lòng chọn tên khác.");
            return;
        }
        dao.create(entity);
        this.fillToTable();
        this.clear();
    }

    @Override
    public void update() {
        NhaCungCap entity = this.getForm();
        dao.update(entity);
        this.fillToTable();
    }

    @Override
    public void delete() {
        if (XDialog.confirm("Bạn thực sự muốn xóa?")) {
            String username = txtUsername.getText();
            dao.deleteById(username);
            this.fillToTable();
            this.clear();
        }
    }

    @Override
    public void clear() {
        this.setForm(new NhaCungCap());
        this.setEditable(false);
    }

